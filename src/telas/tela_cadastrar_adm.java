  private void tratamento(){
        if (txt_apelido.getText().isEmpty() || txt_bi.getText().isEmpty() || txt_morrada.getText().isEmpty()||txt_nome.getText().isEmpty()
                || txt_nuit.getText().isEmpty()|| txt_senha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Insira dados em todos campos para que possa fazer o cadastro ");
        }else{
            String nome_admin;
        String apelido_admin;
        String bi_admin;
        String morrada_admin;
        String senha_admin;
        int id_admin;
        int nuit_admin;

        nome_admin = txt_nome.getText();
        apelido_admin = txt_apelido.getText();
        bi_admin = txt_bi.getText();
        morrada_admin = txt_morrada.getText();
        senha_admin = txt_senha.getText();
        nuit_admin = Integer.parseInt(txt_nuit.getText());
        

        Cadastro_admDTO objcadastro_admDTO = new Cadastro_admDTO();
        objcadastro_admDTO.setNome_admin(nome_admin);
        objcadastro_admDTO.setApelido_admin(apelido_admin);
        objcadastro_admDTO.setBi_admin(bi_admin);
        objcadastro_admDTO.setNuit_admin(nuit_admin);
        objcadastro_admDTO.setMorrada_admin(morrada_admin);
        objcadastro_admDTO.setSenha_admin(senha_admin);

        Cadastro_admDAO objcadastroadmDAO = new Cadastro_admDAO();
        objcadastroadmDAO.adminCadastro(objcadastro_admDTO);

        tela_login objtela_login = new tela_login();
        objtela_login.setVisible(true);

        dispose();
        }
    }
