 private void tratamento(){
        if (txt_apelido_cadastrar.getText().isEmpty() || txt_email_cadastrar.getText().isEmpty() || txt_nome_cadastrar.getText().isEmpty()||txt_senha_cadastrar.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Insira dados em todos campos para que possa fazer o cadastro ");
        }else if(txt_email_cadastrar.getText().contains("@")){
            
        String nome, apelido,email,senha;
        
        nome=txt_nome_cadastrar.getText();
        apelido= txt_apelido_cadastrar.getText();
        email=txt_email_cadastrar.getText();
        senha=txt_senha_cadastrar.getText();
        
        cadastroDTO objcadastroDTO = new cadastroDTO();
        objcadastroDTO.setNome_cadastro(nome);
        objcadastroDTO.setApelido_cadastro(apelido);
        objcadastroDTO.setEmail_cadastro(email);
        objcadastroDTO.setSenha_cadastro(senha);
        
        cadastroDAO objcadastroDAO = new cadastroDAO();
        objcadastroDAO.novocadastro(objcadastroDTO);
        
        tela_login objtela_login= new tela_login();
        objtela_login.setVisible(true);
        
        dispose();
        } else{
            
            JOptionPane.showMessageDialog(null, "o email tem de conter " +"@" + " para que seja valido ");
        }
    }
